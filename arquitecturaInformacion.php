      <h2>Experiencia de Usuario</h2>
      <p class="text-justify">
        Se entiende por experiencia de usuario el conjunto de factores y elementos que determinan la interacción satisfactoria del usuario con un entorno o dispositivo concretos, siendo
        capaces de generar en él un conjunto de emociones positivas sobre el medio y su uso.
      </p>
      <p class="text-justify">
        En la experiencia de usuario interviene la arquitectura de la información, el diseño de interacción, la usabilidad, la accesibilidad, el diseño gráfico, la estética, la psicología
        cognitiva, y la extrapolación de principios del mundo del marketing, entre otras disciplinas. Nathan Shedroff extiende el concepto de experiencia del usuario más allá de la Web
        planteando su <a href="http://nathan.com/information-interaction-design-a-unified-field-theory-of-design/" target="_blank">Teoría unificada del diseño</a> que articula en torno a
        los conceptos clave de diseño de información, percepción e <a href="https://es.wikipedia.org/wiki/Interactividad" target="_blank">interactividad</a>.
      </p>
      <p class="text-justify">
        La arquitectura de la información es una parte específica del marco global más amplio que es la experiencia de usuario.
      </p>
    </article>
    <article class="tema">
      <h2>El Arquitecto de la Información como un equipo de trabajo</h2>
      <p class="text-justify">
        Los autores Sablón Fernández Y. y Hernández Aballe D<sup>3</sup>., proponen que el proceso de la Arquitectura de la Información se lleve a cabo por un equipo interdisciplinario,
        en el que cada miembro juega un rol específico. Los roles que proponen son los siguientes:
      </p>
      <ul>
        <li><p class="text-justify"><b>Compilador de la Información</b>: El compilador de información será el encargado de desarrollar un conjunto de actividades que le permitirán recopilar toda la información
          necesaria para establecer las bases que servirán como guía para el desarrollo de la AI. Para lograr sus objetivos elaborará encuestas y entrevistas centrándose en la organización
          cliente y en los intereses de los futuros usuarios de la aplicación. Su labor se centra en la fase Inicial del proceso.</p></li>
        <li><p class="text-justify"><b>Analista de Contenidos</b>: Se encargará de aplicar un conjunto de técnicas que le permitirán descubrir y diagnosticar las necesidades de información de la comunidad a la
          cual sirve. Debe poseer un conjunto de habilidades como: visión global de todos los elementos de información y su integración, alto conocimiento de las técnicas de categorización
          de la información, buena comunicación y poder de síntesis, capacidad de obtener evidencias del conjunto de información e inferir conclusiones, identificar conexiones entre ideas,
          y ser receptivo a las necesidades de otras personas. Desempeña su trabajo durante las fases Inicial y de Consolidación, y está estrechamente relacionado con el Arquitecto de
          Información.</p></li>
        <li><p class="text-justify"><b>Arquitecto de la Información</b>: Tiene la responsabilidad de llevar a cabo un conjunto de actividades encaminadas a lograr una organización óptima de los contenidos de
          información que se presentarán, así como la forma en que estos serán mostrados a los usuarios. Evalúa, analiza, organiza, reelabora y presenta la información de manera que tenga
          la máxima utilidad para sus destinatarios. Debe tener habilidades para la obtención y análisis de información, orientación al cliente, interés por la innovación y habilidades
          para mantener la atención. Además debe poseer capacidad para agrupar contenidos de temáticas semejantes con las etiquetas más intuitivas posibles, con vista a lograr una mayor
          usabilidad de la aplicación. Su trabajo es imprescindible en el proceso y se desarrollará en las fases de Consolidación y Validación.</p></li>
        <li><p class="text-justify"><b>Ensamblador de Interfaz de Usuario</b>: Su papel fundamental será construir las pantallas de la aplicación desde el punto de vista de la organización e interacción de la
          información, siguiendo en todo momento las pautas recomendadas en el proceso para garantizar la accesibilidad y usabilidad de la aplicación. Como resultado de su trabajo se
          obtiene un boceto gráfico de la estructura informacional de la aplicación que servirá posteriormente como guía a los diseñadores y desarrolladores del proyecto. Específicamente
          realiza su trabajo durante la fase de Consolidación del proceso.</p></li>
        <li><p class="text-justify"><b>Evaluador de Contenidos</b>: Su tarea fundamental será garantizar que el producto final sea fácil de utilizar y comprender, que permita un alto nivel de accesibilidad
          independientemente de las características particulares de cada usuario y del dispositivo usado para hacerlo. Emitirá criterios que evaluarán y permitirán corregir los errores
          encontrados en relación con estos aspectos, logrando satisfacer las necesidades de los usuarios. Realiza su labor durante la fase de Validación.</p></li>
      </ul>
