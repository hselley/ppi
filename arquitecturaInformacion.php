    <article class="tema">
      <h2>Definición</h2>
      <p class="text-justify">
        La arquitectura de la Información (A.I.) es la disciplina y arte encargada del estudio, análisis, organización, disposición y estructuración de la información en espacios de
        información, y de la selección y presentación de los datos en los sistemas de información interactivos y no interactivos.
      </p>
      <p class="text-justify">
        En relación con la World Wide Web, el Information Architecture Institute define la Arquitectura de la Información como:
        <ol>
          <li><p class="text-justify">El diseño estructural en entornos de información compartida.</p></li>
          <li><p class="text-justify">El arte y la ciencia de organizar y rotular sitios web, intranets, comunidades en línea y software para promover la usabilidad y la ubicabilidad (la característica de
            ser encontrado a través de las búsquedas en Internet).</p></li>
          <li><p class="text-justify">Una comunidad emergente orientada a aplicar al entorno digital los principios del diseño y la arquitectura.</p></li>
        </ol>
      </p>
      <p class="text-justify">
        La Arquitectura de la Información trata indistintamente del diseño de: sitios web, interfaces de dispositivos móviles o gadgets (como los lectores de mp3), CD interactivos,
        videoclips digitales, relojes, tableros de instrumentos de aviones de combate o civiles, interfaces de máquinas dispensadoras, interfaces de juegos electrónicos, etc. (Laverde, A. 2005)
      </p>
      <p class="text-justify">
        Su principal objetivo es facilitar al máximo los procesos de comprensión y asimilación de la información, así como las tareas que ejecutan los usuarios en un espacio de información definido.
      </p>
    </article>
    <article class="tema">
      <h2>Arquitectura de la Información como un proceso</h2>
      <p class="text-justify">
        La "arquitectura de la información" es un proceso iterativo, transversal, que se da a lo largo de todo el diseño del sitio y en cada una de sus fases, para asegurarse de que los objetivos
        de su producción y del desarrollo de la interfaz se cumplen de manera efectiva.
      </p>
      <p class="text-justify">
        La Arquitectura de la Información como disciplina no busca definir una metodología de diseño universal sino articular un conjunto de técnicas para ayudar al desarrollo y producción de
        espacios de información como los sitios web.
      </p>
      <p class="text-justify">
        Con el fin de que la asimilación de contenidos por parte del usuario sea eficiente y efectiva, y para que el sitio sea accesible y usable, la Arquitectura de la Información como proceso
        en general se encarga, durante el desarrollo, de definir:
        <ul>
          <li><p class="text-justify">El objeto, propósito y fines del sistema de información o sitio</p></li>
          <li><p class="text-justify">La definición del público objetivo y los estudios de la audiencia</p></li>
          <li><p class="text-justify">La realización de análisis competitivos</p></li>
          <li><p class="text-justify">El diseño de la interacción</p></li>
          <li><p class="text-justify">El diseño de la navegación, esquemas de organización y presentación de los contenidos</p></li>
          <li><p class="text-justify">El etiquetado o rotulado de los contenidos para acceder a la información</p></li>
          <li><p class="text-justify">La planificación, gestión y desarrollo de contenidos</p></li>
          <li><p class="text-justify">La facilidad de búsqueda y el diseño de la interfaz de búsqueda</p></li>
          <li><p class="text-justify">La usabilidad</p></li>
          <li><p class="text-justify">La accesibilidad</p></li>
          <li><p class="text-justify">La retroalimentación del resultado y los procesos de reingeniería del sitio</p></li>
        </ul>
      </p>
    </article>
    <article class="tema">
      <h2>Entregables del Arquitecto de Información</h2>
      <p class="text-justify">
        Normalmente, el trabajo de los Arquitectos de Información se concreta en la generación de un conjunto de materiales entregables (del inglés Deliverables) en los que se plasma de forma
        efectiva la estructura del espacio de información, el diseño de la interacción del usuario con el sistema y el funcionamiento de la interfaz. Estos materiales tienen la doble finalidad
        de servir como material que se suministrará:
        <ul>
          <li><p class="text-justify">A los clientes como resultado del proceso de diseño o reingeniería del sitio llevado a cabo durante la fase de análisis, definición y desarrollo del proyecto.</p></li>
          <li><p class="text-justify">A los diseñadores gráficos como material de base para la producción de maquetas gráficas que utilizarán los maquetadores para la elaboración de las plantillas en
            las que se basarán todas las páginas del sitio web.</p></li>
          <li><p class="text-justify">A los ingenieros informáticos para ayudar a desarrollar la parte de procesamiento y lógica del sitio.</p></li>
        </ul>
    </p>
    </article>
    <article class="tema">
      <h2>Arquitectura de la Información como abstracción</h2>
      <p class="text-justify">
        La Arquitectura de la Información no trata del establecimiento de un conjunto de pasos o guías predefinidas, sino del diseño inteligente que subyace detrás de una interfaz o espacio de
        información. Trata de maximizar las sinergias que se producen entre interactividad, navegación y contenido con el objetivo de alcanzar una integración sistémica con el cerebro del
        usuario logrando un fenómeno de persuasión, conocimiento o información simbiótica que se traspasa de un sistema de información a otro.
      </p>
      <p class="text-justify">
        De esta forma, las capacidades de búsqueda, localización y recuperación de los contenidos se realizan en un contexto óptimo en ambos aspectos del sistema de información (interfaz y usuario),
        interactúan iniciándose un proceso de comunicación que los enriquece mutuamente. Por un lado, la interfaz cumple con su objetivo y puede ser mejorada y, por otro, el usuario encuentra lo
        que busca, lo asimila con facilidad y lo utiliza.
      </p>
    </article>
    <article class="tema">
      <h2>Arquitecto de la Información</h2>
      <p class="text-justify">
        Es la persona encargada de llevar a cabo y verificar el proceso de diseño del sitio; además, trabaja estrechamente con los diseñadores gráficos y los responsables de la parte de procesamiento
        y lógica (back-end) para definirla. Está integrado en un equipo y sus tareas abarcan desde la fundamentación del proyecto hasta el rediseño, verificación y prueba del producto durante todas
        las fases de desarrollo hasta la obtención del resultado final.
      </p>
      <p class="text-justify">
        El A.I. es una nueva profesión que surge en 1996 a raíz de la evolución y transformación de la World Wide Web en un canal y medio de comunicación. Su aparición en un contexto social,
        cultural, económico y político está fuertemente condicionada por las Nuevas Tecnologías de la Información, tecnologías que han modificado bruscamente y a todos los niveles las formas de
        comunicación entre los seres humanos, así como la manera en que perciben y asimilan información.
      </p>
      <p class="text-justify">
        Estos avances en telecomunicaciones, ciencia, y tecnología en general han producido una cantidad ingente de conocimiento, de nuevos conceptos, ideas, métodos, procesos, visiones,
        problemas y soluciones sobre los que interviene la Arquitectura de la Información que, en concreto, busca:
        <ul>
          <li><p class="text-justify">Procesar y dosificar la enorme cantidad de información que se ha producido a causa de los descubrimientos y nuevas investigaciones en todos los nuevos campos surgidos a causa
            de la revolución de Internet, así como ponerla a disposición de una manera clara, relevante y significativa a disposición del usuario común. Se trata, entre otras cosas, de hacer
            comprensible lo abstracto de alguna forma.</p></li>
          <li><p class="text-justify">Desarrollar y verificar procesos de producción o diseño de información con el fin de que el usuario pueda recuperar la información de un determinado espacio de manera clara,
            precisa y sin ambigüedades, en cualquier plataforma o soporte; en concreto, se refiere a soportes multimedia e interactivos, aunque, retomando a Shedroff, en la práctica no
            debemos omitir ningún soporte por plano que este sea y hablar de experiencias de usuario.</p></li>
          <li><p class="text-justify">Organizar, estructurar, sistematizar (Tufte), rotular, distribuir, diseñar estructuralmente sistemas de información (Baeza, Rivera, Velasco, 2003) con el fin de que el usuario
            pueda hacer de su experiencia de recuperación algo simple, agradable, eficaz y productivo.</p></li>
        </ul>
      </p>
    </article>
