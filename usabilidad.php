      <h2><a href="https://www.nngroup.com/articles/top-10-mistakes-web-design/" target="_blank">Errores típicos en el diseño de una página Web.</a></h2>
      <p class="text-justify">
        <ol>
          <li><p class="text-justify">Búsquedas incorrectas.</p></li>
          <li><p class="text-justify">Lectura en línea de archivos PDF.</p></li>
          <li><p class="text-justify">No cambiar el color de links visitados.</p></li>
          <li><p class="text-justify">Demasiado Texto.</p></li>
          <li><p class="text-justify">Texto no escalable.</p></li>
          <li><p class="text-justify">Título de página con poca visibilidad de búsqueda.</p></li>
          <li><p class="text-justify">Todo luce como un anuncio.</p></li>
          <li><p class="text-justify">Violación de los convenios del diseño.</p></li>
          <li><p class="text-justify">Abrir nuevas ventanas del navegador.</p></li>
          <li><p class="text-justify">No responder las preguntas del usuario.</p></li>
          <li><p class="text-justify">Diseñar un página cuya funcionalidad dependa de tecnologías cerradas, por ejemplo Flash, Silverlight o Java (entre otras).</p></li>
          <li><p class="text-justify">Abrir ventanas emergentes (Pop-Up).</p></li>
          <li><p class="text-justify">No tener una versión de la página para dispositivos móviles (responsiva).</p></li>
        </ol>
