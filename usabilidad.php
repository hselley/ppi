      <h2>Como mejorar la Usabilidad.</h2>
      <p class="text-justify">
        Existen algunos métodos para el estudio de la usabilidad, pero el más básico y útil es la prueba de usuario que tiene 3 componentes:
        <ol>
          <li><p class="text-justify">Utilizar como base a usuarios representativos, tales como clientes electrónicos o empleados.</p></li>
          <li><p class="text-justify">Hacer pruebas: pedir a usuarios que realicen tareas representativas en el diseño.</p></li>
          <li><p class="text-justify">Observar lo que hacen los usuarios, notar los lugares donde la interfaz funciona y sobre todo donde no funciona. Callese, escuche,
            deje que los usuarios hablen (Los usuarios siempre tienen la razón, aunque no la tengan).</p></li>
        </ol>
      </p>
      <p class="text-justify">
        Observar lo que hacen los usuarios, notar los lugares donde la interfaz funciona y sobre todo donde no funciona. Callese, escuche, deje que los usuarios hablen
        (Los usuarios siempre tienen la razón, aunque no la tengan).
      </p>
      <p class="text-justify">
        Para identificar los problemas más graves en la usabilidad de un sitio, <a href="https://www.nngroup.com/articles/why-you-only-need-to-test-with-5-users/" target="_blank">basta con 5 usuarios
        de prueba</a>. Lo recomendable es el <a href="https://www.nngroup.com/articles/parallel-and-iterative-design/" target="_blank">diseño iterativo</a> ya que es la mejor forma de incrementar
        la calidad de la experiencia de usuario. Mientras más ideas y diseños de interfaz pruebe con los usuarios, mejor.
      </p>
      <p class="text-justify">
        Para las usuarios que probarán el diseño, es importante no solo utilizar personas muy aptas en esta categoría, la mejor retroalimentación la recibirá de las personas menos aptas. Sin embargo,
        <a href="https://www.nngroup.com/articles/first-rule-of-usability-dont-listen-to-users/" target="_blank">escuchar lo que dice la gente puede ser engañoso</a>, debe poner atención a lo que
        hace el usuario.
      </p>
    </article>

    <article class="tema">
      <h2>Cuando trabajar la Usabilidad</h2>
      <p class="text-justify">
        La usabilidad juega un rol en cada etapa del proceso de diseño. La resultante necesidad para estudios múltiples es una de las razones por las que se recomienda realizar estudios individuales
        rápidos y baratos:
        <ol>
          <li><p class="text-justify">Antes de comenzar un nuevo diseño, pruebe el diseño anterior para identificar las partes que funcionan y deben mantenerse y las partes
            que no sirven. (<b>¡Si no está roto no lo arregle!</b>).</p></li>
          <li><p class="text-justify">Pruebe los diseños de sus competidores para obtener una gama de alternativas de diseño para las interfaces que tienen características
            similares a la suya.</p></li>
          <li><p class="text-justify">Realice un estudio de campo para ver cómo los usuarios se comportan en su hábitat natural.</p></li>
          <li><p class="text-justify">Haga <a href="https://www.nngroup.com/reports/paper-prototyping-training-video/" target="_blank">prototipos en papel</a> de una o más
            ideas de diseño y pongalas a prueba. Mientras menos tiempo le dedique a esto es mejor, ya que estas ideas irán cambiando en función de los resultados.</p></li>
          <li><p class="text-justify">Perfeccione las ideas de diseño que persistan en las iteraciones múltiples, moviéndose gradualmente de un diseño burdo a uno refinado.
            Pruebe cada iteración.</p></li>
          <li><p class="text-justify">Inspeccione su diseño de acuerdo a las <a href="https://www.nngroup.com/reports/topic/user-testing/" target="_blank">guías establecidas
            de diseño</a>, ya sea sus estudios previos o investigación publicada.</p></li>
          <li><p class="text-justify">Una vez que decida e implemente un diseño final, pruébelo de nuevo. Problemas sutiles de usabilidad siempre surgen durante la
            codificación.</p></li>
        </ol>
      </p>
      <p class="text-justify">
        No posponga las pruebas de usuario hasta que termine de codificar el diseño. Le será muy difícil resolver todos los problemas que surjan en su diseño, y algunos de ellos requerirán un
        cambio en el diseño estructural, lo que conlleva mucho trabajo e investigación.
      </p>
    </article>
    <article class="tema">
      <h2>Donde realizar las pruebas.</h2>
      <p class="text-justify">
        Si usted lleva a cabo al menos <a href="https://www.nngroup.com/articles/weekly-user-testing-tivo/" target="_blank">una prueba de usuario por semana</a>, será deseable tener un laboratorio
        exclusivo para ello. Para la mayoría de las compañías basta con una oficina o una sala de conferencias (siempre y cuando pueda cerrar la puerta para evitar distracciones). Lo más importante
        es que usted solicite a usuarios “reales” y los observe mientras utilizan el diseño, usted solo necesita un cuaderno de notas.
      </p>
