    <article class="tema">
      <h2>Definición</h2>
      <p class="text-justify">
        Usabilidad es un atributo de calidad que asegura cuán fácil son de utilizar las interfaces gráficas. La palabra usabilidad se refiere a los métodos para mejorar
        la facilidad de uso durante el proceso de diseño.
      </p>
      <p class="text-justify">
        La usabilidad se define por 5 componentes de calidad:
        <ul>
          <li><p class="text-justify"><b>Intuición</b>. Que tan fácil es para los usuarios completar tareas básicas dado el diseño del sitio.</p></li>
          <li><p class="text-justify"><b>Eficiencia</b>. Una vez que los usuarios han aprendido el diseño, cuan fácil puede realizar alguna tarea específica.</p></li>
          <li><p class="text-justify"><b>Memoria</b>. Cuando los usuarios regresan al sitio luego de un periodo de tiempo, que tan fácil es utilizarlo con destreza.</p></li>
          <li><p class="text-justify"><b>Errores</b>. La cantidad de errores que el usuario comete, que tan graves son y qué tan fácil pueden reponerse de los errores.</p></li>
          <li><p class="text-justify"><b>Satisfacción</b>. Que tan agradable es el diseño del sitio.</p></li>
        </ul>
      </p>
      <p class="text-justify">
        Existen otros importantes atributos de calidad, uno muy importante es la <b>utilidad (uso)</b>, que se refiere a la funcionalidad del diseño.
        ¿El diseño resuelve las necesidades del usuario?
      </p>
      <p class="text-justify">
        Usabilidad y utilidad son igualmente importantes y juntos determinan cuando algo es útil. Importa poco si algo es útil pero no es lo que se desea. Tampoco es
        bueno si el sistema puede hacer hipotéticamente lo que se desea pero la interfaz es muy complicada.
        <ul>
          <li><p class="text-justify"><b>Utilidad</b>: si se provee o no las características deseadas.</p></li>
          <li><p class="text-justify"><b>Usabilidad</b>: Que tan fácil y agradable es utilizar éstas características.</p></li>
          <li><p class="text-justify"><b>Útil</b>: Utilidad + usabilidad.</p></li>
        </ul>
      </p>
      <p class="text-justify"></p>
    </article>

    <article class="tema">
      <h2>Porqué es importante la Usabilidad</h2>
      <p class="text-justify">
        En la Web la usabilidad es una condición necesaria para la supervivencia, si un sitio web es difícil de utilizar la gente lo deja. Si la página falla en establecer
        lo que la compañía ofrece y lo que los usuarios pueden hacer en ella, la gente lo deja. Si los usuarios se pierden en el sitio, la gente lo deja. Si la información
        es difícil de leer o bien no responde las preguntas más importantes de los usuarios, la gente lo deja.
      </p>
      <p class="text-justify">La primer ley del comercio electrónico es que si el usuario no puede encontrar el producto entonces no puede comprarlo.
      </p>
      <p class="text-justify">
        Para las intranet, la usabilidad es una cuestión de la productividad del empleado. Los usuarios perdiendo tiempo debido a una mala usabilidad significa una pérdida
        de dinero para la empresa en cuestión.
      </p>
      <p class="text-justify">
        Con una infinidad de sitios Web en la Internet, los usuarios tienen más opciones que nunca antes. Como consecuencia de esta gran cantidad de opciones los usuarios
        han desarrollado una destacable impaciencia y absurda necesidad de la gratificación instantánea: <b>Considere que el usuario promedio de Internet tiene un alto déficit
        de atención.</b>
      </p>
    </article>
